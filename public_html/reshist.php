      <h3>RESEARCH</h3>
		<p>
			The increasing volumes of data currently being gathered as Big Data is to be analyzed using algorithms from signal processing, communication networks and statistics. The current focus of my research includes transportation of Big Data, efficient use of computing resources to solve data analysis tasks and finally development of models to study the data. This is achieved using a combination of techniques from the fields of communication, data engineering, statistics and machine learning. More details on my research statement can be found <a href="https://www.dropbox.com/s/4v7amyy109mst8q/Research_statement_Immanuel.pdf?dl=0" class="href">here</a>. 
		</p>
      <p><strong>Communication Networks:</strong></p>
      <p>My current research is in the field of communication networks. One branch of research is in developing a queuing model and the second branch is in combining computations with communications. </p>
      <p>Queuing model: </p>
      <p> Here, I'm trying to model a heterogenous network with high speed links using bulk transfer, bulk arrival queuing model with vacations. &nbsp;I try to compare the performance modeling accuracy with existing queuing models. Also, try to answer the following questions:<br>
        <br>
        1. Which nodes in the network are experiencing heavy loads?<br>
        2. If a new source has to join the network, How should the flows be redirected?<br>
        3. If there is a big data transfer operation hogging the bandwidth, what is the steady state loads in the nodes? <br>
        4. Is there an optimal routing and MAC strategy that could be developed using the same?</p>
      <p>Computations and communications:</p>
      <p>FoG nodes are of increasing interest in recent times. Here, we look into the distributed computation capability with communication restricted fog nodes. How can you distribute a data load?, how can you compensate for communication constraints? How can you combine communications with computations? </p>
      <p>&nbsp;</p>
      <p>&nbsp;</p>
      <p>Other research interests are in image retrieval, matchine learning algorithms and methods of statistical analysis like PCA, ICA, and related factor analysis techniques. </p>
